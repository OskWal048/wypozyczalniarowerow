package pl.ioprojekt.wypozyczalniarowerow.helperclasses;

public class EquipmentSizes {

    public String helmetSize;

    public String gogglesSize;

    public String padsSize;

    public String glovesSize;

    public String armorSize;

    public EquipmentSizes() {
    }

    public String getHelmetSize() {
        return helmetSize;
    }

    public void setHelmetSize(String helmetSize) {
        this.helmetSize = helmetSize;
    }

    public String getGogglesSize() {
        return gogglesSize;
    }

    public void setGogglesSize(String gogglesSize) {
        this.gogglesSize = gogglesSize;
    }

    public String getPadsSize() {
        return padsSize;
    }

    public void setPadsSize(String padsSize) {
        this.padsSize = padsSize;
    }

    public String getGlovesSize() {
        return glovesSize;
    }

    public void setGlovesSize(String glovesSize) {
        this.glovesSize = glovesSize;
    }

    public String getArmorSize() {
        return armorSize;
    }

    public void setArmorSize(String armorSize) {
        this.armorSize = armorSize;
    }
}
